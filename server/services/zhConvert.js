// ─── Core function ────────────────────────────────────────────────────────────

/**
 * t2s(text)
 *
 * Convert Traditional Chinese characters in `text` to Simplified Chinese.
 * Non-Chinese characters (ASCII, punctuation, numbers) pass through unchanged.
 *
 * Time complexity: O(n) where n = text length (single-pass).
 *
 * @param {string} text - Input string (may be mixed script)
 * @returns {string}    - Output string with Traditional chars converted
 */
function t2s(text) {
  if (!text || typeof text !== 'string') return text || '';

  // Using Array.from ensures correct handling of Unicode surrogate pairs
  // (e.g., rare CJK Extension B characters above U+FFFF).
  return Array.from(text)
    .map(ch => T2S_MAP[ch] ?? ch) // ?? keeps 0/''/false; undefined/null → original
    .join('');
}

