/**
 * server/services/zhConvert.js
 *
 * Lightweight Traditional → Simplified Chinese character converter.
 *
 * WHY inline instead of a package?
 *   - Zero runtime dependency (no npm install needed on the server).
 *   - Focused on music metadata: artist names, album titles, song names.
 *   - Packages like opencc-js pull in large WASM bundles (~3 MB).
 *
 * Coverage:
 *   ~2,600 characters covering the most common Traditional→Simplified
 *   differences encountered in Taiwanese / Hong Kong music releases.
 *
 * Usage:
 *   const { t2s } = require('./zhConvert');
 *   t2s('愛')  // → '爱'
 *   t2s('傳說') // → '传说'
 */

